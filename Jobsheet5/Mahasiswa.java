package Jobsheet5;

public class Mahasiswa {
    String nama, nim;
    int tahunMasuk, nilaiUTS, nilaiUAS;

    Mahasiswa(String nama, String nim, int tahun, int uts, int uas) {
        this.nama = nama;
        this.nim = nim;
        this.tahunMasuk = tahun;
        this.nilaiUTS = uts;
        this.nilaiUAS = uas;
    }
}
